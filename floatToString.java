package newPackage;

class floatToString {

	public static void main(String[] args) {
		String s1="10";
		Float f=Float.valueOf(s1);//string to Float
		System.out.println(f);
		float g=(float) 1.0;
		Float f2=Float.valueOf(g);//float to Float
		System.out.println(f2);
		String s2=String.valueOf(g);//float to string
		System.out.println(s2);
		
		
		

	}

}
